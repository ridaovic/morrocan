                     <div class="kd-section-title"> <h2>Climat</h2> </div>
                    <p>La ville est sujette au climat méditerranéen. La température moyenne de la ville est de 20°C. À Marrakech, les hivers sont souvent frais le matin et la nuit (6 °C environ), mais les températures y montent très vite en pleine journée pour atteindre les 18 °C en moyenne. Les étés, quant à eux, sont très souvent torrides, avec des températures minimales de 20-22 °C. En journée, il n'est pas rare que le mercure dépasse la barre des 40 °C. Le record de chaleur a été observé le 17 juillet 2012 avec 49,6 °C.
                    Les orages éclatent la plupart du temps vers les mois d'octobre et novembre, car un vent humide et assez rare, le Herrûrco, apparaît en automne, pour y apporter pluies et orages. Quant à l'hiver, il est assez humide et il n'est pas rare qu'il pleuve plusieurs jours de suite. Les montagnes de l'Atlas qui cernent la ville sont enneigées de novembre à avril en moyenne, offrant un magnifique paysage aux portes de Marrakech. Les précipitations sont rares (moins de 25 jours par an). L'ensoleillement annuel y est d'environ 340 jours.</p>
             
