                  <div class="kd-rich-editor">
                    <p>
                      Marrakech, l'une des quatre villes impériales du Maroc, est réellement une ville fascinante. Elle ne saurait laisser indifférent tout qui passe au-delà des murs d'enceinte de la médina, s'étalant sur une distance approximative de 19 kilomètres et dont l'accès se fait par l'une des 9 portes d'entrées. Cette épaisse muraille, Une avenue bordée de palmiers à Marrakech.ou rempart, est de couleur variable selon l'éclairage du moment. Tantôt elle sera de couleur ocre, voire même rouge ou bien rose. Dès votre sortie de l'aéroport, si vous avez choisi l'avion comme moyen de transport, vous serez assailli de mille odeurs dégagées par les fleurs de toutes sortes garnissant les murs de la ville.  Les remparts autour de la ville.Beaucoup de ces murs sont recouverts de bougainvillées aux couleurs mauves, rouges ou bien encore oranges. Les larges avenues qui sillonnent  les extérieurs de la ville sont généralement flanquées, de part et d'autre, de hauts palmiers venant surenchérir la majesté des lieux. En 1979, lors de mon périple en voiture à travers le pays, je suis arrivé à Marrakech venant de Casablanca. En me rapprochant progressivement de la ville, j'apercevais au loin la très célèbre palmeraie de Marrakech, contrastant avec les neiges couvrant les cimes des montagnes de l'Atlas. Les 100.000 palmiers, parfois chargés abondamment de dates selon la saison, s'étendent à perte de vue sur plus de 13.000 hectares. Le spectacle est réellement fascinant, l'on croirait entrer au Paradis ! Les quatre couleurs prédominantes de Marrakech sont le bleu du ciel, le rouge de ses habitations, le vert des palmiers et des oliviers et le blanc des neiges du Haut Atlas.</p>
                  
